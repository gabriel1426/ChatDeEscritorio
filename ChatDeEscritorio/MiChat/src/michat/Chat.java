    private void cmdEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEnviarActionPerformed
      if (!this.txtMensajeEnviar.getText().isEmpty()){
          try {
              this.miNegocio.insertarMensaje(this.txtMensajeEnviar.getText());
          } catch (SQLException ex) {
              Logger.getLogger(Chat.class.getName()).log(Level.SEVERE, null, ex);
          }
         
          this.txtMensajeEnviar.setText("");
          this.cmdLimpiar.setEnabled(true);
      }
    }//GEN-LAST:event_cmdEnviarActionPerformed

