    private void cmdAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAceptarActionPerformed
      
        if (!this.txtUsuario.getText().isEmpty()){
            if(!this.txtUsuario.getText().equalsIgnoreCase("Cambiar de Usuario")){
           if (cont==0){
            this.miNegocio.start();
           cont=1;
           }
            this.miNegocio.DefinirUsuario(this.txtUsuario.getText());
            this.txtMensajeEnviar.setEditable(true);
            this.cmdEnviar.setEnabled(true);
            this.txtMensajeEnviar.setEditable(true);
            this.cmdVoz.setEnabled(true);
            this.txtUsuario.setText("");
          PlaceHolder holder = new PlaceHolder(this.txtUsuario, "Cambiar de Usuario");
          this.miNegocio.limpiar();
        }
        }
    }//GEN-LAST:event_cmdAceptarActionPerformed

